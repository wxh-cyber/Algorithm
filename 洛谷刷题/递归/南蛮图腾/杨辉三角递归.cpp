/**
 *  @link https://www.luogu.com.cn/problem/P1498
 */

 #include <iostream>
 using namespace std;

 const int MAXN=1030;

 int n,line;
 int a[MAXN]={1};                                   //杨辉三角数组，初始化为1


 int main(){
    cin>>n;
    line=1<<n;

    for(int i=0;i<line;i++){
        //输出每一行的前导空格
        for(int j=1;j<line-i;j++){
            cout<<" "; 
        }

        /**
         *  @note
         *    思考：对于两个整数a和b，a^b与(a+b)%2具有相同的效果吗？
         *    答：不完全相同。只在a,b∈{0,1}时等价。
         *        0/1情况下完全一致，都是模2加法。
         *        但对任意整数就不一样了，比如a=3,b=5。
         *            - a^b=3^5=6（按位异或，011^101=110）
         *            - (a+b)%2=8%2=0
         *
         *        一个关键等价关系：(a^b)&1==(a+b)%2    //恒成立
         *        也就是说，a^b的最低位永远等于(a+b)%2——因为异或本就是“不带进位的二进制加法”。
         *        最低位没有进位输入，所以最低位完全等价。
         *  
         *  @note
         *    竞赛中的实用结论：
         *      1.a、b 当布尔值用（0/1）→ a ^ b 和 (a+b)%2 随便用，a^b 更快
         *      2.a、b 是任意整数，只关心奇偶 → 用 ((a^b)&1) 或 ((a+b)&1)，别用 (a+b)%2（取模慢）
         *      3.a、b 是任意整数，需要完整的异或结果 → 只有 a ^ b 对，(a+b)%2 给不出
         */
         for(int j=i;j>=0;j--){
            /** 
             *  @note
             *    由于本题仅采用了杨辉三角的奇偶性，因此可以直接用异或运算，而不用取模运算
             *
             *  @note
             *    从杨辉三角的上一行的最后一个元素开始，依次计算当前行的元素
             *    事实上，杨辉三角的后一行元素比前一行元素多1，但是由于本题已经在初始化时赋值为1，因此可以直接运算。
             *    特别地，当j=0时，a[j-1]会越界，因此需要特判，但通常情况下，值恰好为0，因此可以保证运算的正确性。
             *
             *  @note
             *    注意：这里实际上采用了滚动数组优化，因此从二维数组优化到了一维数组
             *    一维数组需要考虑运算后，会不会污染未处理的数据问题，因此需要从后往前进行处理
             */
            a[j]^=a[j-1];
         }

         //注意i从0到line-1，所以i为偶数时，对应的是奇数行，i为奇数时，对应的是偶数行
         if(!(i%2)){                                                     //实际上的奇数行
            for(int j=0;j<=i;j++){
                cout<<(a[j]?"/\\":" ");
            }
         }else {                                                        //实际上的偶数行
            /**
             *  思考：为什么在偶数行，需要j+=2?
             *  答：可以从字符图形的拼接宽度和杨辉三角模2的数学性质两个方面来解释。
             *      1.图形宽度的对应关系（视觉层面）
             *        在控制台输出字符画时，每一个“小三角形”是由上下两行组成的：
             *            - 三角形顶部（对应代码中 i 为偶数的行）：输出 /\ ，占 2个字符 宽度。
             *            - 三角形底部（对应代码中 i 为奇数的行）：输出 /__\，占 4个字符 宽度。
             *        你可以看出，底部的一个 /__\ 在宽度上正好等于两个顶部的 /\。
             *        在偶数行（i 为奇数），我们需要打印一整行的底部。
             *        因为每个底部占 4 个字符（相当于上一行的 2 个基本单位），所以我们每打印一个图形单位，就需要在数组中跨越 2 个索引（j+=2），这样才能保证图形在水平方向上不会错位。
             *        如果不跳跃 2 个位置，而是像奇数行那样 j++ 逐个打印，那么 /__\（4个字符）就会被挤在一起，图形就完全被破坏了。
             *
             *      2.杨辉三角的模2性质（数学层面）
             *        在奇数行（i 为奇数）中，数组 a 里的数据是“成对相同”的。
             *        回顾一下我们用杨辉三角模 2（异或）生成的数组 a：
             *            - i=0 (偶): [1]
             *            - i=1 (奇): [1, 1] -> a[0] 和 a[1] 相同
             *            - i=2 (偶): [1, 0, 1]
             *            - i=3 (奇): [1, 1, 1, 1] -> a[0]=a[1], a[2]=a[3]
             *            - i=4 (偶): [1, 0, 0, 0, 1]
             *            - i=5 (奇): [1, 1, 0, 0, 1, 1] -> a[0]=a[1], a[2]=a[3], a[4]=a[5]
             *
             *        数学定理：在杨辉三角的第 2𝑘+1行（即奇数行），第 2𝑚个元素和第 2𝑚+1个元素的奇偶性总是相同的。
             *        (注意以上的索引都是从0开始的)
             *        这意味着什么？
             *        在打印偶数行（i 为奇数）时，由于数据成对出现，我们只需要取每对的第一个元素（即偶数索引 a[0], a[2], a[4]...）来判断即可。
             *            - 如果 a[0] 是 1，说明这里有一个三角形，打印 /__\（占4格）。这4格正好覆盖了上一行 a[0] 和 a[1] 组成的两个位置。
             *            - 如果 a[0] 是 0，说明没有三角形，打印 4 个空格。
             *        所以用 j+=2 步长为 2 遍历，既保证了输出字符的宽度比例正确，又巧妙避开了冗余的判断（因为 a[1] 必然和 a[0] 相同，不用再判断一次）。
             */
            for(int j=0;j<=i;j+=2){
                cout<<(a[j]?"/__\\":"    ");
            }
         }

         cout<<endl;
    }


    return 0;
 }